'use client';

import { useEffect, useState } from 'react';
import { useQuery } from '@tanstack/react-query';
import { 
  DocumentTextIcon, 
  EyeIcon, 
  CurrencyDollarIcon, 
  UsersIcon,
  ChartBarIcon,
  ClockIcon
} from '@heroicons/react/24/outline';
import { api } from '@/lib/api';
import DashboardStats from '@/components/admin/DashboardStats';
import RecentPosts from '@/components/admin/RecentPosts';
import TopPosts from '@/components/admin/TopPosts';
import AnalyticsChart from '@/components/admin/AnalyticsChart';
import LoadingSpinner from '@/components/ui/LoadingSpinner';

interface DashboardData {
  posts: {
    total: number;
    published: number;
    draft: number;
    scheduled: number;
    autoGenerated: number;
    manual: number;
  };
  views: {
    total: number;
  };
  revenue: {
    total: number;
  };
  users: {
    total: number;
    active: number;
  };
  recentPosts: Array<{
    _id: string;
    title: string;
    status: string;
    createdAt: string;
    analytics: {
      views: number;
    };
  }>;
  topPosts: Array<{
    _id: string;
    title: string;
    analytics: {
      views: number;
      revenue: number;
    };
    createdAt: string;
  }>;
}

export default function AdminDashboard() {
  const { data: dashboardData, isLoading, error } = useQuery<DashboardData>({
    queryKey: ['admin-dashboard'],
    queryFn: () => api.get('/admin/dashboard').then(res => res.data.data),
    refetchInterval: 30000, // Refetch every 30 seconds
  });

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-64">
        <LoadingSpinner size="lg" />
      </div>
    );
  }

  if (error) {
    return (
      <div className="bg-red-50 border border-red-200 rounded-lg p-4">
        <p className="text-red-800">Failed to load dashboard data</p>
      </div>
    );
  }

  const stats = [
    {
      name: 'Total Posts',
      value: dashboardData?.posts.total || 0,
      icon: DocumentTextIcon,
      change: '+12%',
      changeType: 'positive' as const,
      color: 'blue'
    },
    {
      name: 'Total Views',
      value: dashboardData?.views.total || 0,
      icon: EyeIcon,
      change: '+8%',
      changeType: 'positive' as const,
      color: 'green'
    },
    {
      name: 'Total Revenue',
      value: `$${(dashboardData?.revenue.total || 0).toFixed(2)}`,
      icon: CurrencyDollarIcon,
      change: '+15%',
      changeType: 'positive' as const,
      color: 'yellow'
    },
    {
      name: 'Active Users',
      value: dashboardData?.users.active || 0,
      icon: UsersIcon,
      change: '+5%',
      changeType: 'positive' as const,
      color: 'purple'
    }
  ];

  return (
    <div className="space-y-6">
      {/* Page Header */}
      <div>
        <h1 className="text-2xl font-bold text-gray-900">Dashboard</h1>
        <p className="mt-1 text-sm text-gray-500">
          Welcome back! Here's what's happening with your blog.
        </p>
      </div>

      {/* Stats Cards */}
      <DashboardStats stats={stats} />

      {/* Charts and Analytics */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
        <div className="bg-white rounded-lg shadow p-6">
          <h3 className="text-lg font-medium text-gray-900 mb-4">Analytics Overview</h3>
          <AnalyticsChart />
        </div>
        
        <div className="bg-white rounded-lg shadow p-6">
          <h3 className="text-lg font-medium text-gray-900 mb-4">Content Distribution</h3>
          <div className="space-y-4">
            <div className="flex items-center justify-between">
              <div className="flex items-center">
                <DocumentTextIcon className="h-5 w-5 text-blue-500 mr-2" />
                <span className="text-sm text-gray-600">Published Posts</span>
              </div>
              <span className="text-sm font-medium text-gray-900">
                {dashboardData?.posts.published || 0}
              </span>
            </div>
            <div className="flex items-center justify-between">
              <div className="flex items-center">
                <ClockIcon className="h-5 w-5 text-yellow-500 mr-2" />
                <span className="text-sm text-gray-600">Draft Posts</span>
              </div>
              <span className="text-sm font-medium text-gray-900">
                {dashboardData?.posts.draft || 0}
              </span>
            </div>
            <div className="flex items-center justify-between">
              <div className="flex items-center">
                <ChartBarIcon className="h-5 w-5 text-green-500 mr-2" />
                <span className="text-sm text-gray-600">Auto-Generated</span>
              </div>
              <span className="text-sm font-medium text-gray-900">
                {dashboardData?.posts.autoGenerated || 0}
              </span>
            </div>
            <div className="flex items-center justify-between">
              <div className="flex items-center">
                <UsersIcon className="h-5 w-5 text-purple-500 mr-2" />
                <span className="text-sm text-gray-600">Manual Posts</span>
              </div>
              <span className="text-sm font-medium text-gray-900">
                {dashboardData?.posts.manual || 0}
              </span>
            </div>
          </div>
        </div>
      </div>

      {/* Recent Posts and Top Posts */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-6">
        <RecentPosts posts={dashboardData?.recentPosts || []} />
        <TopPosts posts={dashboardData?.topPosts || []} />
      </div>
    </div>
  );
}
